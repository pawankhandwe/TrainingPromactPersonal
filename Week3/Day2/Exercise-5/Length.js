function getLengths(input) {
    return input.len
}
// Example usage
var strLengths = getLengths("Hello, TypeScript!"); // Length of the string
var arrLengths = getLengths([1, 2, 3, 4, 5,"sd"]); // Length of the array
console.log("String length:", strLengths); // Output: String length: 19
console.log("Array length:", arrLengths); // Output: Array length: 5
